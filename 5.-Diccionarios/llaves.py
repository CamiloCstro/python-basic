diccionario = {'camilo': 24, 'Andres': 25, 'Juan': 32}

# keys imprimir las llaves del diccionario
for keys in diccionario:
    print(keys)

print(diccionario.keys())

# values
# imprime los valores del diccionario
print(diccionario.values())

# items
# Se recomienda convertir a tuplas para que no se puedan modificar los valores.
# imprime las llaves y valores del diccionario
print(diccionario.items())
